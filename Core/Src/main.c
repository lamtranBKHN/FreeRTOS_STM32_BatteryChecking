/**
  * @brief  Function implementing the mainTask thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_StartMainTask */
void StartMainTask(void const * argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
	  osSemaphoreWait(lowBattSemHandle, osWaitForever);
	  HAL_GPIO_TogglePin(led_GPIO_Port, led_Pin);
      osDelay(100);
      if(battVoltage > lowBatteryVoltage) osSemaphoreRelease(lowBattSemHandle);
  }
  /* USER CODE END 5 */ 
}

/* USER CODE BEGIN Header_StartCheckBattery */
/**
* @brief Function implementing the checkBattery thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartCheckBattery */
void StartCheckBattery(void const * argument)
{
  /* USER CODE BEGIN StartCheckBattery */
  /* Infinite loop */
  for(;;)
  {
	  HAL_ADC_Start(&hadc3);
	  battVoltage = HAL_ADC_GetValue(&hadc3);
	  if(battVoltage > lowBatteryVoltage) osSemaphoreRelease(lowBattSemHandle);
      osDelay((uint32_t)batteryCheckingPeriod);
  }
  /* USER CODE END StartCheckBattery */
}

